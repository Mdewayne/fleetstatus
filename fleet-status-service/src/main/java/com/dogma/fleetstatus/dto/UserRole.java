package com.dogma.fleetstatus.dto;

/**
 * Роли пользователей в системе мониторинга автопарка
 * 
 * DRIVER - водитель: базовый доступ к информации о своем ТС
 * MANAGER - менеджер: расширенный доступ к данным автопарка
 * ADMIN - администратор: полный доступ ко всем данным и функциям
 */
public enum UserRole {
    DRIVER,
    MANAGER,
    ADMIN
} 